int validparams(int channel, int hbank, int lbank, int prog)
{
  if (channel < 0 || channel > 15)
    return 0;
  if (prog < 0 || prog > 127)
    return 0;
  if (hbank < 0 || hbank > 127)
    return 0;
  if (lbank < 0 || lbank > 127)
    return 0;
  return 1;
}

const char* findpatch(MIDIPATCH* table, int count, int bank, int prog)
{
  if (!table)
    return 0;
  for (int i = 0; i < count; i++, table++)
  {
    if (prog == table->program)
    {
      if (bank == table->bank)
	return table->patchname;
    }
  }
  return 0;
}

const char* gmpatch(int channel, int hbank, int lbank, int prog)
{
  hbank = lbank = 0;
  if (prog < 0)
    prog = 0;
  if (!validparams(channel, hbank, lbank, prog))
    return 0;
  if (channel == 9) // gm drums
    return gmdrumname;
  return gmprogname[prog & 127];
}

const char* gspatch(int channel, int hbank, int lbank, int prog)
{
  if (hbank < 0)
    hbank = 0;
  if (lbank < 0)
    lbank = 0;
  if (prog < 0)
    prog = 0;
  if (!validparams(channel, hbank, lbank, prog))
    return 0;
  if (channel == 9) // gm drums
  {
    for (int i = 0; i < GSDRUMS; i++)
    if (prog == gsdrumname[i].program)
      return gsdrumname[i].patchname;
  }
  else
  {
  const char* name = 0;

    if (lbank == 0)
      name = findpatch(gsprogname, GSPROGRAMS, hbank, prog);
    if (name)
      return name;
  }
  return 0;
}

const char* xgpatch(int channel, int hbank, int lbank, int prog)
{
const char* name;

  if (hbank < 0)
    hbank = (channel == 9) ? 0x7f : 0;
  if (lbank < 0)
    lbank = 0;
  if (prog < 0)
    prog = 0;
  if (!validparams(channel, hbank, lbank, prog))
    return 0;
  if (channel != 9)
  {
    name = findpatch(xgprogname, XGPROGRAMS, lbank, prog);
    if (name)
      return name;
  }
  name = findpatch(xgdrumname, XGDRUMS, hbank, prog);
  if (name)
    return name;
  return 0;
}

const char* patch(int channel, int hbank, int lbank, int prog, MidiMode mode)
{
  const char* pname = 0;
  switch(mode)
  {
  case MODE_GS:  pname = gspatch(channel, hbank, lbank, prog); break;
  case MODE_XG:  pname = xgpatch(channel, hbank, lbank, prog); break;
  case MODE_GM:
  default:       break;
  }
  if (!pname)
    return gmpatch(channel, hbank, lbank, prog);
  return pname;
}
